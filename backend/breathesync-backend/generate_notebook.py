            "# For Random Forest, we flatten the features (or take mean over time)\n",
            "def get_rf_features(X):\n",
            "    # X shape: (samples, time_steps, features)\n",
            "    # We take the mean across time_steps to get (samples, features)\n",
            "    return np.mean(X, axis=1)\n",
            "\n",
            "def build_cnn(input_shape, num_classes=4):\n",
            "    model = Sequential([\n",
            "        Input(shape=input_shape),\n",
            "        Conv1D(64, kernel_size=3, activation='relu', padding='same'),\n",
            "        MaxPooling1D(pool_size=2),\n",
            "        Conv1D(128, kernel_size=3, activation='relu', padding='same'),\n",
            "        MaxPooling1D(pool_size=2),\n",
            "        Conv1D(256, kernel_size=3, activation='relu', padding='same'),\n",
            "        MaxPooling1D(pool_size=2),\n",
            "        GlobalAveragePooling1D(),\n",
            "        Dropout(0.5),\n",
            "        Dense(128, activation='relu'),\n",
            "        Dense(num_classes, activation='softmax')\n",
            "    ])\n",
            "    model.compile(optimizer='adam', loss='categorical_crossentropy', metrics=['accuracy'])\n",
            "    return model\n",
            "\n",
            "\n",
            "class AttentionLayer(tf.keras.layers.Layer):\n",
            "    def __init__(self, **kwargs):\n",
            "        super(AttentionLayer, self).__init__(**kwargs)\n",
            "\n",
            "    def build(self, input_shape):\n",
            "        self.W = self.add_weight(name='attention_weight', \n",
            "                                 shape=(input_shape[-1], 1), \n",
            "                                 initializer='random_normal', \n",
            "                                 trainable=True)\n",
            "        self.b = self.add_weight(name='attention_bias', \n",
            "                                 shape=(input_shape[1], 1), \n",
            "                                 initializer='zeros', \n",
            "                                 trainable=True)\n",
            "        super(AttentionLayer, self).build(input_shape)\n",
            "\n",
            "    def call(self, x):\n",
            "        # x: (batch_size, time_steps, features)\n",
            "        e = tf.keras.backend.tanh(tf.keras.backend.dot(x, self.W) + self.b)\n",
            "        a = tf.keras.backend.softmax(e, axis=1)\n",
            "        output = x * a\n",
            "        return tf.keras.backend.sum(output, axis=1)\n",
            "\n",
            "def build_bilstm_attention(input_shape, num_classes=4):\n",
            "    inputs = Input(shape=input_shape)\n",
            "    x = Bidirectional(LSTM(128, return_sequences=True))(inputs)\n",
            "    x = Dropout(0.3)(x)\n",
            "    x = Bidirectional(LSTM(64, return_sequences=True))(x)\n",
            "    x = AttentionLayer()(x)\n",
            "    x = Dense(64, activation='relu')(x)\n",
            "    x = Dropout(0.4)(x)\n",
            "    outputs = Dense(num_classes, activation='softmax')(x)\n",
            "    \n",
            "    model = Model(inputs, outputs)\n",
            "    model.compile(optimizer='adam', loss='categorical_crossentropy', metrics=['accuracy'])\n",
            "    return model\n"
