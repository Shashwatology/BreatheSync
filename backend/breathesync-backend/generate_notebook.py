            "        return np.array([]), np.array([])\n",
            "        \n",
            "    for txt_file in tqdm(txt_files, desc=\"Processing files\"):\n",
            "        wav_file = txt_file.replace('.txt', '.wav')\n",
            "        if not os.path.exists(wav_file):\n",
            "            continue\n",
            "            \n",
            "        try:\n",
            "            audio, _ = librosa.load(wav_file, sr=SR)\n",
            "        except Exception as e:\n",
            "            print(f\"Error loading {wav_file}: {e}\")\n",
            "            continue\n",
            "            \n",
            "        annotations = pd.read_csv(txt_file, sep='\\t', header=None, names=['start', 'end', 'crackles', 'wheezes'])\n",
            "        \n",
            "        for _, row in annotations.iterrows():\n",
